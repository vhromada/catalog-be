package com.github.vhromada.catalog.entity

/**
 * A class represents statistics for music.
 *
 * @author Vladimir Hromada
 */
class MusicStatistics(

    /**
     * Count of music
     */
    val count: Int,

    /**
     * Count of songs
     */
    val songsCount: Int,

    /**
     * Count of media
     */
    val mediaCount: Int,

    /**
     * Length
     */
    val length: String

)
